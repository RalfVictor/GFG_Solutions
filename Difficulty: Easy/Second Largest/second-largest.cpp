class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        int n = arr.size();
        sort(arr.begin(),arr.end());
        int x = count(arr.begin(),arr.end(),arr[n-1]);
        if(x==n){
            return -1;
        }
        return arr[n-x-1];
    }
};

