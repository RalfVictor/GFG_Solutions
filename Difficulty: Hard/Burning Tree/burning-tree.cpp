class Solution {
  public:
  
    int minTime(Node* root, int targets) {
        int ans = -1;
        if(targets==NULL) return 0;
        unordered_map<Node*, Node*>parent_tracker;
        Node* target = NULL;
        queue<Node*>qu;
        qu.push(root);
        while(!qu.empty()){
            Node* node= qu.front();
            if(node->data == targets){
                target = node;
            }
            qu.pop();
            if(node->left){
                parent_tracker[node->left]=node;
                qu.push(node->left);
            }
            if(node->right){
                parent_tracker[node->right]=node;
                qu.push(node->right);
            }
        }
        unordered_map<Node* , bool>visited;
        queue<Node*>q;
        q.push(target);
        visited[target]=true;
        while(!q.empty()){
            int siz= q.size();
            for(int i=0;i<siz;i++){
                Node* current= q.front();
                q.pop();
                if(current->left && !visited[current->left]){
                    q.push(current->left);
                    visited[current->left]=true;
                }
                if(current->right && !visited[current->right]){
                    q.push(current->right);
                    visited[current->right]=true;
                }
                if(parent_tracker[current] && !visited[parent_tracker[current]]){
                    q.push(parent_tracker[current]);
                    visited[parent_tracker[current]]=true;
                }

            }
            ans++;

        }
        return ans;
    }
};


